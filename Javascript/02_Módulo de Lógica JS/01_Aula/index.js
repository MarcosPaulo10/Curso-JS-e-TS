/*
Operadores de comparação:

> maior que
>= maior que ou igual a
< menor que
<= menor que ou igual a
=== igualdade estrita (valor e tipo)
!== diferente estrito (valor e tipo)

*/

const num1 = 8;
const num2 = 2;

let comp = num1 !== num2;
console.log(comp);

comp = num1 > num2;
console.log(comp);

comp = num1 >= num2;
console.log(comp);

comp = num1 <= num2;
console.log(comp);

comp = num1 < num2;
console.log(comp);

comp = num1 == num2;
console.log(comp);

